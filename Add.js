function add(x, y){
    return x + y;
}

function subtract(x, y){
    return x - y;
}

function divide(x, y){
    return x / y;
}

function multiply(x, y){
    return x * y;
}

console.log(add(2,3));
console.log(subtract(5,2));
console.log(divide(9,2));
console.log(multiply(4,5));